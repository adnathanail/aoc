from .utils import combine_maps, do_map_lookup


def test_combine_maps():
    map1 = [[0, 50, 10], [50, 100, -20]]
    map2 = [[10, 60, 5], [60, 110, -10]]
    assert combine_maps(map1, map2) == [[0, 50, 15], [50, 80, -15], [80, 100, -30]]


def test_complex_maps():
    map3 = [[-100, 0, 0], [0, 7, 81], [7, 11, 89], [11, 15, 28], [15, 18, -26], [18, 25, 44], [25, 50, -33],
            [50, 53, -31], [53, 54, -24], [54, 61, -9], [61, 95, -5], [95, 98, 2], [98, 100, -48], [100, 200, 0]]
    map4 = [[-100, 0, 0], [0, 45, 0], [45, 64, 36], [64, 77, 4], [77, 100, -32], [100, 200, 0]]
    assert combine_maps(map3, map4) == [[-100, 0, 0], [0, 7, 49], [7, 11, 57], [11, 15, 28], [15, 18, -26],
                                        [18, 20, 80], [20, 25, 48], [25, 33, -33], [33, 50, -33], [50, 53, -31],
                                        [53, 54, -24], [54, 61, 27], [61, 69, 31], [69, 82, -1], [82, 95, -37],
                                        [95, 98, -30], [98, 100, -12], [100, 200, 0]]
